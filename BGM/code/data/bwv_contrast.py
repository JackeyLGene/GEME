FREQ_MIN, FREQ_MAX, BINS = 65, 1050, 27

def hz_vec(notes):
    v = [0.0]*BINS
    for n in notes:
        f = NOTES_Hz.get(n, 440)
        idx = int((f-FREQ_MIN)/(FREQ_MAX-FREQ_MIN)*(BINS-1))
        v[max(0,min(BINS-1,idx))] += 1.0
    s = sum(v)
    return [x/s for x in v] if s > 0 else v

